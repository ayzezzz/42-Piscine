#include <unistd.h>

void	ft_putchr(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putchr('-');
		ft_putchr('2');
		ft_putnbr(147483648);
	}
	else if (nb < 0)
	{
		ft_putchr('-');
		nb = -nb;
		ft_putnbr(nb);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		ft_putchr(nb + 48);
	}
}
